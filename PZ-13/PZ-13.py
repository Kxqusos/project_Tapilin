"""
Из исходного текстового файла (price.txt) выбрать все цены. Посчитать количество полученных элементов
"""

import re

with open("price.txt", "r") as f:
    text = f.read()

r"""
\d+    — одна или более цифр        → 100
\s*    — ноль или более пробелов    → ' ' или ''
руб    — буквально "руб"            → руб
\.?    — точка (необязательная)     → '.' или ''
\s*    — ноль или более пробелов    → ' ' или ''
\d*    — ноль или более цифр        → 55 или ''
\s*    — ноль или более пробелов
коп    — буквально "коп"
\.?    — точка (необязательная)
"""
regex_pattern = r'\d+\s*руб\.?\s*\d*\s*коп\.?'
prices = re.findall(regex_pattern, text)

print("Найденные цены:")
for i, price in enumerate(prices, 1):
    print(f"  {i}. {price.strip()}")

print(f"\nКоличество найденных цен: {len(prices)}")